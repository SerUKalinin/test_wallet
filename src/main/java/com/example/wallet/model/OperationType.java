package com.example.wallet.model;

/**
 * Перечисление типов операций с кошельком.
 * Используется для определения характера транзакции:
 * - DEPOSIT: пополнение кошелька
 * - WITHDRAW: снятие средств с кошелька
 */
public enum OperationType {
    /**
     * Операция пополнения кошелька
     */
    DEPOSIT,
    
    /**
     * Операция снятия средств с кошелька
     */
    WITHDRAW
} 